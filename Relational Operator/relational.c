


#include<stdio.h>

int main()
{
    int a=10,b=5;
    
    printf("\n Equal to                  : %d",a==b);  //0  false
    printf("\n Greater than              : %d",a>b);  //0  true
    printf("\n Lesser than               : %d",a<b);  //0  false
    printf("\n Greater than or equal to  : %d",a>=b);  //0  true
    printf("\n Lesser than or equal to   : %d",a<=b);  //0  false
    printf("\n Not equal to              : %d",a!=b);  //0  true

   
    return 0;
}